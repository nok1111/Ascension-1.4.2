function onCastSpell(creature, var)

if not creature then
return false
end

local p = {monster = creature, var = var, combat = combat}
creature:say("DIE HUMANS!", TALKTYPE_ORANGE_2)
for i = 1, #area_garona do
       -- combat[i]:setFormula(COMBAT_FORMULA_LEVELMAGIC, 0, -min, 0, -max)
        if i == 1 then
            combat[i]:execute(creature:getId(), var)
        else
            addEvent(executeCombat_garona, (animationDelay * i) - animationDelay, p, i)
			
        end
    end
	return true
end
