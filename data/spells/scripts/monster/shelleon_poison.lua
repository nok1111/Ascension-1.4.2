function onTargetCreature(creature, target)
	creature:addDamageCondition(target, CONDITION_POISON, DAMAGELIST_VARYING_PERIOD, 200, {2, 3}, 50)
	if target:isPlayer() then
	creature:sendExtendedOpcode(76, "You are poisoned, find a way to remove the poison!")	
	end
end

combataoe:setCallback(CALLBACK_PARAM_TARGETCREATURE, "onTargetCreature")
