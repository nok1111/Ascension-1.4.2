local function castcombatwarning(creatureId, variant)
	local creature = Creature(creatureId)
	if not creature then
		return
	end
	
    combatwarning:execute(creature, variant)
end

local function castcombataoe(creatureId, variant)
	local creature = Creature(creatureId)
	if not creature then
		return
	end
	
    combataoe:execute(creature, variant)
end

function onCastSpell(creature, variant)

if not creature then
return false
end
  local startingPosition = variant.position -- Assuming 'position' is the key in the variant table representing the starting position
creature:say("You Are disturbing the Runes!", TALKTYPE_ORANGE_2)

local position = creature:getPosition()
  local positioneffect = position
    positioneffect.x = position.x + 1
    positioneffect.y = position.y + 1
    positioneffect:sendMagicEffect(364)
   
   combatwarning:execute(creature, variant)
   addEvent(castcombatwarning, 1500, creature:getId(), variant, startingPosition)
   addEvent(castcombataoe, 3000, creature:getId(), variant, startingPosition)



  return true
end
