petExpByLevel = {
    [1] = 500,
    [2] = 1500,
    [3] = 3000,
    [4] = 6200,
    [5] = 9500,
    [6] = 13000,
    [7] = 15000,
    [8] = 20000,
    [9] = 30000,
    [10] = 50000
}

function getPetMaxExpByLevel(level)
    if level > 0 and level <= 10 then
        return petExpByLevel[level]
    else
        return petExpByLevel[10] -- cap pet level at 10 if it goes beyond
    end
end

-- pets.lua

function addPetExp(master, exp)
    if not master then
        return false
    end
    
    local currentExp = master:getStorageValue(STORAGEVALUE_PET_EXP)
    local petLevel = master:getStorageValue(STORAGEVALUE_PET_LVL)

    Player.setPetExp(master, exp)
    
    if currentExp >= getPetMaxExpByLevel(petLevel) then
        addPetLevel(master, 1)
    end
    
    return true
end

function addPetLevel(master, levelsToAdd)
    if not master then
        return false
    end
    
    local currentLevel = master:getStorageValue(STORAGEVALUE_PET_LVL)
    local newLevel = currentLevel + levelsToAdd

    if newLevel > 10 then
        newLevel = 10 -- Cap pet level at 10
    end
    
    master:setStorageValue(STORAGEVALUE_PET_LVL, newLevel)
    return true
end
